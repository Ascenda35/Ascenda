function setLanguage(lang) {
  localStorage.setItem('ascenda-lang', lang);
  document.documentElement.lang = lang;
  
  if (lang === 'zh-CN') {
    document.body.classList.add('simplified');
  } else {
    document.body.classList.remove('simplified');
  }
  
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  // Update placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (translations[lang] && translations[lang][key]) {
      el.placeholder = translations[lang][key];
    }
  });

  // Update language switcher active state
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });
}

function getCurrentLanguage() {
  return localStorage.getItem('ascenda-lang') || 'zh-HK';
}

function translate(key, lang = null) {
  const targetLang = lang || getCurrentLanguage();
  return translations[targetLang]?.[key] || key;
}

function initializeLanguage() {
  const savedLang = getCurrentLanguage();
  setLanguage(savedLang);
}

// Auto-initialize when DOM is ready
document.addEventListener('DOMContentLoaded', initializeLanguage);

// Export functions for use in other modules
export { setLanguage, getCurrentLanguage, translate, translations };
export default { setLanguage, getCurrentLanguage, translate, translations };
